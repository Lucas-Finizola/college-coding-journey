#Mostrar a sua média(que pode não ser inteira).

print("Cálculadora de Média entre 5 números")
var1 = int(input("Digite o primeiro valor:"))
var2 = int(input("Digite o segundo valor:"))
var3 = int(input("Digite o terceiro valor:"))
var4 = int(input("Digite o quarto valor:"))
var5 = int(input("Digite o quinto valor:"))

media = (var1+var2+var3+var4+var5)/5
print(f"A media cálculada é de {media}")
