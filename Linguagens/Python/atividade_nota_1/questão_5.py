"""
Area e perimetro de um retangulo
Area = b*h
perimetro = P=2(b+h)
"""

print("Cálcular a Area e o Perimetro de um retangulo")
b = int(input("Digite o valor da base do retangulo: "))
h = int(input("Digite o valor da altura do retangulo: "))

A = b*h
P = 2*(b+h)
print(f"A area é de {A}\nO perimetro é de {P}")
